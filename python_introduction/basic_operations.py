number1 = 10
number2 = 5
# Sum   
sum_result = number1 + number2
#print the sum
print ("Sum of ", number1, "and", number2, "is:", sum_result)
# Subtraction
sub_result = number1 - number2
#print the subtraction
print ("Difference of ", number1, "and", number2, "is:", sub_result)
# Multiplication
mul_result = number1 * number2
#print the multiplication
print ("Product of ", number1, "and", number2, "is:", mul_result)